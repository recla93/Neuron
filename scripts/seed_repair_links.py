"""Seed repair: remove dangling links (97% of links reference nonexistent keywords).

The seed was generated by Graphify which used internal identifiers (snake_case)
for link sources/targets that don't match the keyword display names in the nodes table.
Only 268/8626 links are valid — these are between display-name keywords like
"Spring Boot" -> "Spring Bean". All others are noise for keyword-based graph navigation.

This script removes all dangling links, keeping only valid ones.
Run once, then commit the updated seed.
"""
import os, sys

SEED = os.path.normpath(os.path.join(os.path.dirname(__file__), "..", "knowledge", "base_knowledge.db"))

from neuron import db as sqlite3

try:
    from fastembed import TextEmbedding
    _embedder = TextEmbedding("sentence-transformers/all-MiniLM-L6-v2")
    FASTEMBED = True
except ImportError:
    FASTEMBED = False


def main():
    before = os.path.getsize(SEED)
    conn = sqlite3.connect(SEED)

    total = conn.execute("SELECT COUNT(*) FROM links").fetchone()[0]
    valid = conn.execute("""
        SELECT COUNT(*) FROM links l
        WHERE l.source IN (SELECT keyword FROM nodes)
        AND l.target IN (SELECT keyword FROM nodes)
    """).fetchone()[0]
    dangling = total - valid

    print(f"Links: {total} total, {valid} valid, {dangling} dangling ({dangling/total*100:.0f}%)")

    # Delete dangling links
    conn.execute("""
        DELETE FROM links WHERE source NOT IN (SELECT keyword FROM nodes)
        OR target NOT IN (SELECT keyword FROM nodes)
    """)
    conn.commit()

    after_links = conn.execute("SELECT COUNT(*) FROM links").fetchone()[0]
    print(f"Deleted {dangling} dangling links. Remaining: {after_links}")

    # VACUUM to reclaim space (no-op / unsupported on the Turso engines)
    if not sqlite3.LOCAL_TURSO_ENGINE and not sqlite3.REMOTE_TURSO:
        conn.execute("VACUUM")

    after = os.path.getsize(SEED)
    conn.close()
    print(f"Size: {before/1024:.0f}KB -> {after/1024:.0f}KB (saved {(before-after)/1024:.0f}KB)")
    print(f"Updated: {SEED}")


if __name__ == "__main__":
    main()
